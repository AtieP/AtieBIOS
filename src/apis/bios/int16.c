static int caps_lock = 0;
static int shift = 0;

static void handle_00(struct apis_bios_regs *regs) {
    uint8_t scancode;
    while (1) {
        scancode = ps2_keyboard_get_scancode();
        if (scancode == 0x2a || scancode == 0x36) {
            shift = 1;
        } else if (scancode == 0xaa || scancode == 0xb6) {
            shift = 0;
        } else if (scancode == 0x3a) {
            if (caps_lock) {
                caps_lock = 0;
            } else {
                caps_lock = 1;
            }
        } else if (!(scancode & (1 << 7))) {
            break;
        }
    }
    regs->ah = scancode;
    if (caps_lock && shift) {
        regs->al = mapping_shift_caps[scancode];
    } else if (caps_lock && !shift) {
        regs->al = mapping_caps[scancode];
    } else if (!caps_lock && shift) {
        regs->al = mapping_shift[scancode];
    } else {
        regs->al = mapping_nocaps[scancode];
    }
}

void apis_bios_int16(struct apis_bios_regs *regs) {
    if (regs->ah == 0x00) {
        handle_00(regs);
    } else {
        print("lakebios: bios: handler not available.");
        for (;;) {}
    }
}
